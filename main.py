Q3 = df['Математика'].quantile(0.75)
IQR = Q3 - Q1
print(f"Результат рассчета IQR для математики: {IQR}")

print(f"Результат рассчета стандартного отклонения для математики: {df['Математика'].std()}")
print(f"Результат рассчета стандартного отклонения для русского языка: {df['Русский язык'].std()}")
print(f"Результат рассчета стандартного отклонения для физики: {df['Физика'].std()}")
print(f"Результат рассчета стандартного отклонения для информатики: {df['Информатика'].std()}")
